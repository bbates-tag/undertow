let enabled = true;
export function setSfxEnabled(on: boolean) {
  enabled = on;
}

const lastPlayed: Record<string, number> = {};

export function playSfx(name: string) {
  if (!enabled) return;
  const params = S[name];
  if (!params) return;
  // throttle identical sounds triggered in the same instant (multi-hit spam)
  const now = performance.now();
  if (now - (lastPlayed[name] ?? 0) < 45) return;
  lastPlayed[name] = now;
  try {
    zzfx(...params);
  } catch {
    // audio not unlocked yet — ignore
  }
}

export function playSfxList(names: string[]) {
  names.forEach((n, i) => {
    if (i === 0) playSfx(n);
    else setTimeout(() => playSfx(n), Math.min(i * 70, 500));
  });
}
